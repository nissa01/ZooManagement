package tn.esprit.gestionzoo.entities;

import tn.esprit.gestionzoo.exceptions.ZooFullException;

public class Zoo {

    public static final int NUMBER_OF_CAGES = 2;
    private Animal[] animals;
    private String name, city;
    private int nbrAnimals;

    public Zoo() {
        animals = new Animal[NUMBER_OF_CAGES];
    }

    public Zoo(String name, String city) {
        this();
        this.name = name;
        this.city = city;
    }

    public static Zoo comparerZoo(Zoo z1, Zoo z2) {
        if (z1.nbrAnimals > z2.nbrAnimals)
            return z1;
        return z2;
    }

    public Animal[] getAnimals() {
        return animals;
    }

    public void setAnimals(Animal[] animals) {
        this.animals = animals;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        if (name.isBlank())
            System.out.println("The Zoo name cannot be empty");
        else
            this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getNbrAnimals() {
        return nbrAnimals;
    }

    public void setNbrAnimals(int nbrAnimals) {
        this.nbrAnimals = nbrAnimals;
    }

    void displayZoo() {
        System.out.println("Name: " + name + ", City: " + city + ", N째 Cages: " + NUMBER_OF_CAGES + " N째 animals: " + nbrAnimals);
    }

    public void addAnimal(Animal animal) throws ZooFullException {
        if (searchAnimal(animal) != -1) {
            System.out.println("Animal already exists in the zoo.");
            return;
        }
        if (isZooFull()) {
            throw new ZooFullException("The zoo is full. Cannot add more animals.");
        }
        animals[nbrAnimals] = animal;
        nbrAnimals++;
        System.out.println("Added animal: " + animal.getName() + ". Total animals: " + nbrAnimals);
    }

    public boolean removeAnimal(Animal animal) {
        int indexAnimal = searchAnimal(animal);
        if (indexAnimal == -1)
            return false;
        for (int i = indexAnimal; i < nbrAnimals - 1; i++) {
            animals[i] = animals[i + 1];
        }
        animals[nbrAnimals - 1] = null;
        this.nbrAnimals--;
        return true;
    }

    public void displayAnimals() {
        System.out.println("List of animals of " + name + ":");
        for (int i = 0; i < nbrAnimals; i++) {
            System.out.println(animals[i]);
        }
    }

    public int searchAnimal(Animal animal) {
        int index = -1;
        for (int i = 0; i < nbrAnimals; i++) {
            if (animal.getName().equals(animals[i].getName()))
                return i;
        }
        return index;
    }

    public boolean isZooFull() {
        return nbrAnimals == NUMBER_OF_CAGES;
    }

    @Override
    public String toString() {
        return "Zoo{ Name: " + name + ", City: " + city + ", N째 Cages: " + NUMBER_OF_CAGES + " N째 animals: " + nbrAnimals + "}";
    }
}
